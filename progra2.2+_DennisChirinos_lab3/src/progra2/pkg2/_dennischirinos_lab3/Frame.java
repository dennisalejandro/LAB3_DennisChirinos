    private void jMenuItem4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem4ActionPerformed
        jDialog4.pack();
        jDialog4.setVisible(true);
        CB_JD4_Equipos.removeAllItems();
        for (int i = 0; i < listEquipo.size(); i++) {
            CB_JD4_Equipos.addItem(listEquipo.get(i).getNombre());
        }
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("Nombre");
        model.addColumn("Apellido");
        model.addColumn("Edad");
        model.addColumn("Estado");
        model.addColumn("Pais");
        model.addColumn("PiePreferido");
        model.addColumn("Equipo");
        model.addColumn("NvlDefinicion");
        model.addColumn("Altura");
        model.addColumn("Velocidad");
        model.addColumn("Goles");

        try {
            Equipo Eq = listEquipo.get(CB_JD4_Equipos.getSelectedIndex());
            ArrayList<Delantero> list = Eq.getDelanteros();

            if (list.size() >= 0) {
                Object[] o = new Object[model.getColumnCount()];
                for (int i = 0; i < list.size(); i++) {
                    o[0] = list.get(i).getNombre();
                    o[1] = list.get(i).getApellido();
                    o[2] = list.get(i).getEdad();
                    o[3] = list.get(i).getEstado();
                    o[4] = list.get(i).getPais();
                    o[5] = list.get(i).getPiePreferido();
                    o[6] = list.get(i).getEquipo();
                    o[7] = list.get(i).getNvlDefinicion();
                    o[8] = list.get(i).getAltura();
                    o[9] = list.get(i).getVelocidad();
                    o[10] = list.get(i).getGoles();
                    model.addRow(o);
                }
            }
        } catch (Exception e) {
        }

        Tabla.setModel(model);
    }//GEN-LAST:event_jMenuItem4ActionPerformed

